# bool (boolean) data type
# When questioning something in a program
# there are only two possible answers: yes or no
# There are several logical operators to question, among them: ==, !=, >, <, >=, <=
# The result of a logical operator is always true or false

print(1 == 1) # True
print(1 == 2) # False
print(type(1 == 1)) # <class 'bool'>